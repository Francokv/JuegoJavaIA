/*
    Vector2 es una clase que encapsula 2 números enteros, x e y los cuales se utilizan para representar una posición en un espacio de 2 dimensiones
*/
public class Vector2Int {
    public int x;
    public int y;

    public Vector2Int() {
    }

    public Vector2Int(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
